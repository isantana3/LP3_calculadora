    public void igualA(){
        switch (getOperando1()) {
            case "+":
                this.resultado = number1 + number2;
                setNumber2(0); //att o valor de $number 2
                break;
            case "-":
                this.resultado = number1 - number2;
                setNumber2(0); //att o valor de $number 2
                break;
            case "*":
                this.resultado = number1 * number2;
                setNumber2(1); //att o valor de $number 2
                break;
            case "/":
                this.resultado = number1 / number2;  
                setNumber2(1); //att o valor de $number 2
                break;
        }
        this.setControle(1);
        this.operando1 = this.operando2; //é útil para a  função igualOperação()
        this.operando2 = "#"; //zera o sinal da segunda operação
    }
    
