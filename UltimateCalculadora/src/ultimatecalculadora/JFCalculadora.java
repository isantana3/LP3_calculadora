    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if (this.operando1.equals("#")) { // verifica se esse é o primeiro ou o segndo operador da expressão
            this.setOperando1("/");
            this.number1 = Double.parseDouble(mainOut.getText());  // salva o número digitado
            mainOut.setText(mainOut.getText() + "/"); //mostra qual operação iremos fazer
            this.setControle(1); // seta a variável de controle como 1, para que limpe a tela durante o próximo input
            
        }else{
            this.number2 = Double.parseDouble(mainOut.getText());  // salva o número digitado
            this.setOperando2("/");
            this.igualOperação(); //função que faz as 4 operações básicas
            mainOut.setText(this.getNumber1() + this.getOperando1()); // mostra o resultado e a próxima operação a ser realizada
        }
    }//GEN-LAST:event_jButton3ActionPerformed

